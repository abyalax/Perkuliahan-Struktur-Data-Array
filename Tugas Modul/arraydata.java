public class arraydata {
    public static void main(String[] args) {
        String[] nama = new String[5];
        nama[0] = "Dani";
        nama[1] = "Dio";
        nama[2] = "Dandi";

        for(int i = 0; i < nama.length; i++) {
            System.out.println(nama[i] + " ");
        }
        System.out.println("");
    }
}
